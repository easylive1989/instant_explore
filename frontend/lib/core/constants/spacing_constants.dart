/// Spacing constants for consistent layout throughout the app.
///
/// Provides a standardized spacing system following the 4px base unit.
class AppSpacing {
  AppSpacing._();

  /// Extra small spacing (4px)
  static const double xs = 4.0;

  /// Small spacing (8px)
  static const double sm = 8.0;

  /// Medium spacing (16px) - Default spacing
  static const double md = 16.0;

  /// Large spacing (24px)
  static const double lg = 24.0;

  /// Extra large spacing (32px)
  static const double xl = 32.0;

  /// Extra extra large spacing (48px)
  static const double xxl = 48.0;

  /// 時間軸相關間距
  static const double timelineLineWidth = 2.0;
  static const double timelineDotSize = 12.0;
  static const double timelineLeftPadding = 40.0;
  static const double timelineCardIndent = 56.0;
}
